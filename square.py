def area(a):
    """
    Вычисляет площадь квадрата.
    
    Параметры:
        a - длина стороны квадрата
    
    Пример вызова:
        area(4)
        Результат: 16
    """
    return a * a

def perimeter(a):
    """
    Вычисляет периметр квадрата.
    
    Параметры:
        a - длина стороны квадрата
    
    Пример вызова:
        perimeter(4)
        Результат: 16
    """
    return 4 * a
