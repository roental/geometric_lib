def area(a, h):
    """
    Вычисляет площадь треугольника.
    
    Параметры:
        a - длина основания
        h - высота треугольника
    
    Пример вызова:
        area(10, 5)
        Результат: 25.0
    """
    return a * h / 2

def perimeter(a, b, c):
    """
    Вычисляет периметр треугольника.
    
    Параметры:
        a, b, c - длины сторон треугольника
    
    Пример вызова:
        perimeter(3, 4, 5)
        Результат: 12
    """
    return a + b + c
