def area(a, b):
    """
    Вычисляет площадь прямоугольника.
    
    Параметры:
        a - длина первой стороны
        b - длина второй стороны
    
    Пример вызова:
        area(5, 10)
        Результат: 50
    """
    return a * b

def perimeter(a, b):
    """
    Вычисляет периметр прямоугольника.
    
    Параметры:
        a - длина первой стороны
        b - длина второй стороны
    
    Пример вызова:
        perimeter(5, 10)
        Результат: 30
    """
    return 2 * (a + b)
